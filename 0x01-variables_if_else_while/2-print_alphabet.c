#include <stdio.h>

/**
 * main - This prints the alphabet in lowercase, followed by a new line
 * Return: 0 (Success)
 */

int main(void)
{
	    char i = 'a';

	        while (i <= 'z')
			    {
				            putchar(i);
					            i++;
						        }

		    putchar('\n');

		        return (0);
}
